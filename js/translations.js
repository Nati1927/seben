// Language management class
class LanguageManager {
    constructor() {
        this.currentLanguage = 'en';
        this.init();
    }
    
    init() {
        const savedLanguage = localStorage.getItem('seben-language');
        if (savedLanguage && translations[savedLanguage]) {
            this.currentLanguage = savedLanguage;
        }
        this.applyTranslations();
        this.updateLanguageToggle();
    }
    
    switchLanguage(language) {
        if (translations[language]) {
            this.currentLanguage = language;
            localStorage.setItem('seben-language', language);
            this.applyTranslations();
            this.updateLanguageToggle();
        }
    }
    
    applyTranslations() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[this.currentLanguage][key]) {
                if (element.tagName === 'INPUT' && element.type === 'submit') {
                    element.value = translations[this.currentLanguage][key];
                } else if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
                    element.placeholder = translations[this.currentLanguage][key];
                } else {
                    element.innerHTML = translations[this.currentLanguage][key];
                }
            }
        });
        
        document.documentElement.setAttribute('lang', this.currentLanguage);
    }
    
    updateLanguageToggle() {
        const languageToggle = document.getElementById('languageToggle');
        if (languageToggle) {
            languageToggle.textContent = this.currentLanguage === 'en' ? 'EN' : 'አማ';
        }
    }
}

window.languageManager = new LanguageManager();